# 4. Multiline String

# How can you assign the following rhyme to a 
# single variable while preserving the line 
# break?

# use triple quoted string
pirate_song = """A pirate I was meant to be!
Trim the sails and roam the sea!"""

print(pirate_song)
# A pirate I was meant to be!
# Trim the sails and roam the sea!

# LS:
rhyme = "A pirate I was meant to be!\nTrim the sails and roam the sea!"

print(rhyme)
# A pirate I was meant to be!
# Trim the sails and roam the sea!